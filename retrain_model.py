# 텍스트와 라벨 분리
X_train = [text for text, label in training_data]
y_train = [label for text, label in training_data]

# 한국어 특성을 고려한 간단한 띄어쓰기 기준 토크나이저 함수
def simple_korean_tokenizer(text):
    # 특수문자 제거 후 단어 단위 분리
    clean_text = "".join([c for c in text if c.isalnum() or c.isspace()])
    return clean_text.split()

print("🔄 [ML 엔진] 5대 감정 하이브리드 파이프라인을 위한 머신러닝 모델 재학습 시작...")

# 💡 TF-IDF 벡터라이저와 나이브 베이즈 모델을 파이프라인으로 결합
# 단어 빈도뿐만 아니라 '취업', '전공' 같은 핵심 어휘의 가중치(TF-IDF)를 극대화합니다.
model_pipeline = Pipeline([
    ('tfidf', TfidfVectorizer(tokenizer=simple_korean_tokenizer, min_df=1)),
    ('clf', MultinomialNB(alpha=0.1)) # 평활화 가중치 조절로 예측 예민도 업그레이드
])

# 모델 학습
model_pipeline.fit(X_train, y_train)

# 저장할 폴더 경로 설정 (counseling/ml_model/)
